marks = int(input("Enter your Marks : "))
if marks>=90:
    print("Grade A")
    print("Excellent")
elif marks>=80:
    print("Grade B")
    print("Good Job!")
elif marks>=70:
    print("Grade c")
    print("Keep Improving!")
elif marks>=60:
    print("Grade D")
    print("Work Hard!")
else:
    print("Fail")
    print("Better luck next time")

